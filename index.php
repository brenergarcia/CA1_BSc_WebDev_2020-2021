              <h2>PHP History</h2>
              <img src="img/php-logo.png" width="20%" height="10%">
              <p class="text-justify">PHP development began in 1994 when Rasmus Lerdorf wrote several Common Gateway Interface (CGI) programs in C, 
                which he used to maintain his personal homepage. He extended them to work with web forms and to communicate with databases, 
                and called this implementation "Personal Home Page/Forms Interpreter" or PHP/FI.
                PHP/FI could be used to build simple, dynamic web applications. To accelerate bug reporting and improve the code, Lerdorf initially 
                announced the release of PHP/FI as "Personal Home Page Tools (PHP Tools) version 1.0" on the Usenet discussion group comp.infosystems.www.authoring.cgi on June 8, 1995.
                This release already had the basic functionality that PHP has today. This included Perl-like variables, form handling, and the ability to embed HTML. 
                The syntax resembled that of Perl, but was simpler, more limited and less consistent.
